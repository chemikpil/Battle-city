BattleCity.Map = function (game) {
  this.game = game;
  this.map = map;
  this.tile = {
    w: 16,
    h: 16
  }
  this.cols = this.game.size.w / this.tile.w;
  this.rows = this.game.size.h / this.tile.h;
  
  return this;
};

BattleCity.Map.prototype = {
  draw: function (screen) {
    var sprite = this.game.assets.getAsset('img/map.png');
    var col = 0;
    var row = 0;
    
    for (var i = 0, l = this.map.length; i < l;) {
      for (var j = 0, k = this.cols; j < k; j++) {
        if (this.map[i] > 0) {
          screen.drawImage(sprite, 
            16 * (this.map[i] - 1), 0, 
            this.tile.w, this.tile.h, 
            this.tile.w * j, this.tile.h * row,
            this.tile.w, this.tile.h
          ); 
        }
        i++;
      }
      row++;
    }
  }
};